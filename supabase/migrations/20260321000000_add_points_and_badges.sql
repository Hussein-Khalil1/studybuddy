-- user_course_points: tracks CCR points per user per course (capped at 1000)
create table if not exists public.user_course_points (
  user_id  uuid   not null references public.profiles(id) on delete cascade,
  course_id bigint not null references public.courses(id)  on delete cascade,
  points   integer not null default 0 check (points >= 0 and points <= 1000),
  updated_at timestamptz not null default timezone('utc', now()),
  primary key (user_id, course_id)
);

alter table public.user_course_points enable row level security;

drop policy if exists "Own course points" on public.user_course_points;
create policy "Own course points"
  on public.user_course_points for select to authenticated
  using (auth.uid() = user_id);

drop policy if exists "Group co-members can read course points" on public.user_course_points;
create policy "Group co-members can read course points"
  on public.user_course_points for select to authenticated
  using (
    exists (
      select 1
      from public.group_memberships gm1
      join public.group_memberships gm2 on gm1.group_id = gm2.group_id
      where gm1.user_id = auth.uid()
        and gm2.user_id = user_course_points.user_id
    )
  );

drop policy if exists "Insert own course points" on public.user_course_points;
create policy "Insert own course points"
  on public.user_course_points for insert to authenticated
  with check (auth.uid() = user_id);

drop policy if exists "Update own course points" on public.user_course_points;
create policy "Update own course points"
  on public.user_course_points for update to authenticated
  using (auth.uid() = user_id);


-- user_badges: milestone badges at 250 / 500 / 750 / 1000 pts per course
create table if not exists public.user_badges (
  id        bigint generated by default as identity primary key,
  user_id   uuid   not null references public.profiles(id) on delete cascade,
  course_id bigint not null references public.courses(id)  on delete cascade,
  badge_level integer not null check (badge_level between 1 and 4),
  earned_at  timestamptz not null default timezone('utc', now()),
  unique (user_id, course_id, badge_level)
);

alter table public.user_badges enable row level security;

drop policy if exists "Own badges" on public.user_badges;
create policy "Own badges"
