create table if not exists public.course_enrollments (
  user_id uuid not null references public.profiles(id) on delete cascade,
  course_id bigint not null references public.courses(id) on delete cascade,
  created_at timestamptz not null default timezone('utc'::text, now()),
  primary key (user_id, course_id)
);

create index if not exists course_enrollments_course_id_idx on public.course_enrollments (course_id);

alter table public.courses enable row level security;
alter table public.course_enrollments enable row level security;

drop policy if exists "Authenticated users can read courses" on public.courses;
create policy "Authenticated users can read courses"
  on public.courses
  for select
  to authenticated
  using (true);

drop policy if exists "Authenticated users can read course enrollments" on public.course_enrollments;
create policy "Authenticated users can read course enrollments"
  on public.course_enrollments
  for select
  to authenticated
  using (true);

drop policy if exists "Users can insert own course enrollments" on public.course_enrollments;
create policy "Users can insert own course enrollments"
  on public.course_enrollments
  for insert
  to authenticated
  with check (auth.uid() = user_id);

drop policy if exists "Users can delete own course enrollments" on public.course_enrollments;
create policy "Users can delete own course enrollments"
  on public.course_enrollments
  for delete
  to authenticated
  using (auth.uid() = user_id);

drop policy if exists "Users can read own profile" on public.profiles;
drop policy if exists "Users can read own or shared-course profiles" on public.profiles;
create policy "Users can read own or shared-course profiles"
  on public.profiles
  for select
  to authenticated
  using (
    auth.uid() = id
    or exists (
      select 1
      from public.course_enrollments as mine
      join public.course_enrollments as theirs
        on mine.course_id = theirs.course_id
      where mine.user_id = auth.uid()
        and theirs.user_id = profiles.id
    )
  );
